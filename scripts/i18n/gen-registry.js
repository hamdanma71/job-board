#!/usr/bin/env node
/**
 * Auto-generates the i18n registry from the dictionary files so the platform
 * self-adapts: drop a new src/locales/<code>.ts file (and optionally a localeMeta
 * entry for a nice label) and everything — type, LOCALES, DICTS, switcher, admin,
 * <html dir>, the QA gate — picks it up with NO further code edits.
 *
 * Emits TWO files:
 *   src/lib/locales.generated.ts   (client-safe: codes + label/dir, NO dict imports)
 *   src/locales/dicts.generated.ts (server-only: static dict imports + DICTS map)
 *
 * Run via `npm run i18n:gen` (also runs in prebuild before the audit).
 */
const fs = require("fs");
const path = require("path");
const { LOCALES_DIR, readConfig } = require("./_load");

const cfg = readConfig();
const meta = cfg.localeMeta || {};
const rtl = new Set(cfg.rtl || ["ar"]);
const defaultLocale = cfg.defaultLocale || cfg.baseLocale || "ar";

// Discover dictionary codes from the files (the single source of truth).
const codes = fs
  .readdirSync(LOCALES_DIR)
  .filter((f) => f.endsWith(".ts") && !f.endsWith(".generated.ts"))
  .map((f) => f.replace(/\.ts$/, ""));

if (!codes.includes(defaultLocale)) {
  console.error(`gen-registry: defaultLocale "${defaultLocale}" has no src/locales/${defaultLocale}.ts`);
  process.exit(1);
}

// Stable order: configured localeMeta order first, then any extra drop-in files (alpha).
const ordered = [
  ...Object.keys(meta).filter((c) => codes.includes(c)),
  ...codes.filter((c) => !meta[c]).sort(),
];

const metaFor = (c) => ({
  code: c,
  label: (meta[c] && meta[c].label) || c.toUpperCase(),
  dir: (meta[c] && meta[c].dir) || (rtl.has(c) ? "rtl" : "ltr"),
});

const HEADER = "// AUTO-GENERATED by scripts/i18n/gen-registry.js — DO NOT EDIT BY HAND.\n// Regenerate with `npm run i18n:gen` (also runs in prebuild). Source: src/locales/*.ts + scripts/i18n/i18n.config.json.\n";

// 1) client-safe registry (no dictionary imports — safe for the language switcher bundle)
const localesTs =
  HEADER +
  "\nexport const LOCALES = [\n" +
  ordered.map((c) => { const m = metaFor(c); return `  { code: ${JSON.stringify(m.code)}, label: ${JSON.stringify(m.label)}, dir: ${JSON.stringify(m.dir)} },`; }).join("\n") +
  "\n] as const;\n\nexport type Locale = (typeof LOCALES)[number][\"code\"];\n\nexport const DEFAULT_LOCALE: Locale = " + JSON.stringify(defaultLocale) + ";\n";

// 2) server-only dictionaries map
const dictsTs =
  HEADER +
  "\n" +
  ordered.map((c) => `import { ${c} } from "./${c}";`).join("\n") +
  '\nimport type { Locale } from "@/lib/locales";\n\nexport const DICTS: Record<Locale, Record<string, string>> = {\n' +
  ordered.map((c) => `  ${c},`).join("\n") +
  "\n};\n";

fs.writeFileSync(path.join(LOCALES_DIR, "..", "lib", "locales.generated.ts"), localesTs);
fs.writeFileSync(path.join(LOCALES_DIR, "dicts.generated.ts"), dictsTs);

console.log(`gen-registry: ${ordered.length} locales [${ordered.join(", ")}] -> src/lib/locales.generated.ts + src/locales/dicts.generated.ts (default: ${defaultLocale})`);
